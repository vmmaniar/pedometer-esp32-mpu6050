static esp_err_t cmd(uint8_t c)
{
    uint8_t buf[2] = { 0x00, c };
    return i2c_master_write_to_device(s_port, s_addr, buf, 2, pdMS_TO_TICKS(50));
}

esp_err_t ssd1306_init(const ssd1306_config_t *cfg)
{
    s_port = cfg->port;
    s_addr = cfg->address ? cfg->address : SSD1306_DEFAULT_ADDR;

    static const uint8_t init_seq[] = {
        0xAE, 0xD5, 0x80, 0xA8, 0x3F, 0xD3, 0x00, 0x40,
        0x8D, 0x14, 0x20, 0x00, 0xA1, 0xC8, 0xDA, 0x12,
        0x81, 0xCF, 0xD9, 0xF1, 0xDB, 0x40, 0xA4, 0xA6, 0xAF,
    };
    for (size_t i = 0; i < sizeof(init_seq); i++) {
        esp_err_t err = cmd(init_seq[i]);
        if (err != ESP_OK) { ESP_LOGE(TAG, "init cmd %u failed", (unsigned)i); return err; }
    }

    memset(s_buf, 0, sizeof(s_buf));
    return ssd1306_flush();
}

void ssd1306_clear(void)
{
    memset(s_buf, 0, sizeof(s_buf));
}

void ssd1306_draw_char(int col, int page, char c)
{
    if (col < 0 || col >= SSD1306_WIDTH - 5) return;
    if (page < 0 || page >= SSD1306_PAGES) return;

    const uint8_t *glyph = NULL;
    if (c >= '0' && c <= '9') glyph = DIGITS[c - '0'];
    else if (c >= 'A' && c <= 'Z') glyph = LETTERS[c - 'A'];
    else if (c >= 'a' && c <= 'z') glyph = LETTERS[c - 'a'];

    uint8_t blank[5] = {0};
    if (!glyph) glyph = blank;

    for (int i = 0; i < 5; i++) {
        s_buf[page * SSD1306_WIDTH + col + i] = glyph[i];
    }
}

void ssd1306_draw_string(int col, int page, const char *s)
{
    int x = col;
    while (*s) {
        ssd1306_draw_char(x, page, *s++);
        x += 6;
    }
}

esp_err_t ssd1306_flush(void)
{
    static const uint8_t addr_cmds[] = { 0x21, 0x00, 0x7F, 0x22, 0x00, 0x07 };
    for (size_t i = 0; i < sizeof(addr_cmds); i++) {
        esp_err_t err = cmd(addr_cmds[i]);
        if (err != ESP_OK) return err;
    }

    // 0x40 prefix says "data follows"; send pages in chunks
    uint8_t chunk[1 + 64];
    chunk[0] = 0x40;
    for (int p = 0; p < SSD1306_PAGES; p++) {
        for (int x = 0; x < SSD1306_WIDTH; x += 64) {
            memcpy(&chunk[1], &s_buf[p * SSD1306_WIDTH + x], 64);
            esp_err_t err = i2c_master_write_to_device(s_port, s_addr, chunk, sizeof(chunk),
                                                       pdMS_TO_TICKS(50));
            if (err != ESP_OK) return err;
        }
    }
    return ESP_OK;
}
